class Command(BaseCommand):
    help = "Наполняет сайт ИПМ контентом со старых сайтов"

    def img(self, rel, title):
        if not rel:
            return None
        path = OLD / rel
        if not os.path.exists(path):
            self.stdout.write(self.style.WARNING(f"нет файла: {rel}"))
            return None
        existing = Image.objects.filter(title=title).first()
        if existing:
            return existing
        with open(path, "rb") as f:
            image = Image(title=title, file=ImageFile(f, name=os.path.basename(path)))
            image.save()
        return image

    def child(self, parent, klass, slug, title, **fields):
        page = klass.objects.filter(slug=slug).first()
        if page:
            for k, v in fields.items():
                setattr(page, k, v)
            page.title = title
            page.save_revision().publish()
            return page
        page = klass(title=title, slug=slug, **fields)
        parent.add_child(instance=page)
        page.save_revision().publish()
        return page

